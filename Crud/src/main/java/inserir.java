    private void Fetch(){
        try {
            int q;
            pst = con.prepareCall("SELECT * FROM cadastrados");
            rs = pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            q = rss.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)conteudo.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int a = 1; a <= q; a++){
                    v2.add(rs.getString("nome"));
                    v2.add(rs.getString("telefone"));
                    v2.add(rs.getString("email"));
                    v2.add(rs.getString("estado"));
                }
                df.addRow(v2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(inserir.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
   private void pesquisar() {
    try {
        String searchTerm = txtpesquisa.getText();
        DefaultTableModel model = (DefaultTableModel) conteudo.getModel();
        model.setRowCount(0); // Limpa a tabela antes de preencher com os resultados da pesquisa

        String query = "SELECT * FROM cadastrados WHERE nome LIKE ? OR telefone LIKE ? OR email LIKE ? OR estado LIKE ?";
        pst = con.prepareStatement(query);
        pst.setString(1, "%" + searchTerm + "%");
        pst.setString(2, "%" + searchTerm + "%");
        pst.setString(3, "%" + searchTerm + "%");
        pst.setString(4, "%" + searchTerm + "%");

        rs = pst.executeQuery();

        while (rs.next()) {
            String nome = rs.getString("nome");
            String telefone = rs.getString("telefone");
            String email = rs.getString("email");
            String estado = rs.getString("estado");

            model.addRow(new Object[]{nome, telefone, email, estado});
        }
    } catch (SQLException ex) {
        Logger.getLogger(inserir.class.getName()).log(Level.SEVERE, null, ex);
    }
}

