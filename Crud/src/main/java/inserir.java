    private void txtcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcadastrarActionPerformed
        try {
    String nome = txtnome.getText();
    String cep = txtcep.getText();
    String endereco = txtendereco.getText();
    String n = txtn.getText();
    String telefone = txttelefone.getText();
    String celular = txtcelular.getText();
    String email = txtemail.getText();
    
    String query = "INSERT INTO cadastrados (nome, cep, endereco, n, estado, telefone, celular, email) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    pst = con.prepareStatement(query);
    pst.setString(1, nome);
    pst.setString(2, cep);
    pst.setString(3, endereco);
    pst.setString(4, n);
    pst.setString(5, txtestado.getSelectedItem().toString()); // Supondo que 'estado' é um ComboBox
    pst.setString(6, telefone);
    pst.setString(7, celular);
    pst.setString(8, email);

    int k = pst.executeUpdate();

    if(k == 1){
        JOptionPane.showMessageDialog(this, "INDIVÍDUO CADASTRADO COM SUCESSO!!!");
        txtnome.setText("");
        limparCampos();
        Fetch();
    }
    else{
        JOptionPane.showMessageDialog(this, "INDIVÍDUO NÃO FOI CADASTRADO!!!");
    }
} catch (SQLException ex) {
    Logger.getLogger(inserir.class.getName()).log(Level.SEVERE, null, ex);
}

    }//GEN-LAST:event_txtcadastrarActionPerformed

