    private void campos() {
        try {
            // Estabelecer conexão com o banco de dados (exemplo com MySQL)
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/clientes", "root", "");

            // Consulta para obter informações com base no nomeSelecionado
            String sql = "SELECT endereco, cep, n, estado, telefone, celular, email FROM cadastrados WHERE nome = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, nomeSelecionado);
            ResultSet result = statement.executeQuery();

            // Preencher os campos se a consulta retornar resultados
            if (result.next()) {
                txttelefone.setText(result.getString("telefone"));
                txtcelular.setText(result.getString("celular"));
                txtcep.setText(result.getString("cep"));
                txtendereco.setText(result.getString("endereco"));
                txtemail.setText(result.getString("email"));
                txtn.setText(result.getString("n"));
                txtestado.setSelectedItem(result.getString("estado"));
            }

            // Fechar conexão
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
            // Trate a exceção adequadamente na sua aplicação
        }
    }
