    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
       try {
        // Estabelecer conexão com o banco de dados (exemplo com MySQL)
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/clientes", "root", "");

        // Query de atualização para modificar os dados no banco de dados
        String sql = "UPDATE cadastrados SET endereco=?, cep=?, n=?, estado=?, telefone=?, celular=?, email=? WHERE nome=?";
        PreparedStatement statement = conn.prepareStatement(sql);
        
        // Configurar os parâmetros com os valores dos campos de texto
        statement.setString(1, txtendereco.getText());
        statement.setString(2, txtcep.getText());
        statement.setString(3, txtn.getText());
        statement.setString(4, txtestado.getSelectedItem().toString());
        statement.setString(5, txttelefone.getText());
        statement.setString(6, txtcelular.getText());
        statement.setString(7, txtemail.getText());
        statement.setString(8, nomeSelecionado); // Utilize o nomeSelecionado como condição WHERE

        // Executar a atualização
        int rowsUpdated = statement.executeUpdate();
        
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(null, "Os dados de cadastro foram atualizados com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            System.out.println("Os dados foram atualizados no banco de dados.");
            // Você pode adicionar uma mensagem de sucesso ou atualizar a interface de acordo com a necessidade
        }

        // Fechar conexão
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
        // Trate a exceção adequadamente na sua aplicação
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_salvarActionPerformed

